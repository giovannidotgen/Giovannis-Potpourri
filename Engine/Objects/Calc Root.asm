; ---------------------------------------------------------------------------
; INPUT:
; d0.w	= Number
; OUTPUT:
; d0.w	= Root of number
; USES:
; d0, d1
; ---------------------------------------------------------------------------

; =============== S U B R O U T I N E =======================================

Calc_Root:
		moveq	#-1,d1

.find
		addq.w	#2,d1
		sub.w	d1,d0
		bhs.s	.find

		subq.w	#1,d1
		lsr.w	d1
		exg	d1,d0
		rts