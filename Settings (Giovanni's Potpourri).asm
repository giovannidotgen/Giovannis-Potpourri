; ===========================================================================
; Custom settings for Giovanni's Potpourri
; ===========================================================================

EnableDropDash		= 1	; Allows Sonic to perform the Drop Dash.
FastAirMoves		= 1	; Allows Sonic to perform air moves at any jump height.
OPTailsFlight		= 0 ; Prevents Tails' initial flight speed from being capped if you turn on FastAirMoves, making Tails' flight exceptionally overpowered.
EnablePeelOut		= 1	; Allows Sonic to perform the Super Peel-Out.
PeelOut_NoMinimumCharge	= 1 ; The Super Peel-Out can be released any time.
PeelOut_RunSprites	= EnablePeelOut	; Makes Sonic use his Peel-Out sprites when running extra fast.